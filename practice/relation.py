# Relational Operators Or Comparison Operators (<,<=,>,>=,==,!=)
#Returns only Boolean values 
# Used to compare two values

num1=100
num2=200

print(num1>num2) #False 

print(num2>num1) #True 

print(num1>=num2) #False

print(num2>=num1) #True

print(num1<=num2) #True

print(num2<=num1) #False

print(num1==num2) # False

print(num1!=num2) #True

